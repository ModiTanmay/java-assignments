package java__practise;

public class Increament_decreament_operator {

	public static void main(String[] args) 
	{
          int i=60;
          System.out.println(i++);//It's print first and then increse the value
          System.out.println(i);
          System.out.println(++i);//It's increase  first then print the value 
          System.out.println(i);
          
	}

}
